import math

a = int(input("Enter integer a: "))
b = int(input("Enter integer b: "))

print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} % {b} = {a % b}")
print(f"log10({a}) = {math.log10(a)}")
print(f"{a}^{b} = {a ** b}")